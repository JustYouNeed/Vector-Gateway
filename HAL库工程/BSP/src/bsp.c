# include "bsp.h"



/*
*********************************************************************************************************
*                                          bsp_Config
*
* Description: 板级资源初始化函数，初始化如按键、定时器、串口等常用功能
*             
* Arguments  : 无
*
* Reutrn     : 无
*
* Note(s)    : 该函数的初始化功能可在bsp.h里面配置，去掉不需要使用的功能
*********************************************************************************************************
*/
void bsp_Config(void)
{	
	bsp_tim_SoftConfig();
# if OS_SUPPORT < 1u
	bsp_tim_CreateTimer(1,20,TIMER_MODE_AUTO);
	bsp_tim_SetTimerCB(1,bsp_key_Scan);
# endif
	
# if USE_KEY > 0u
	bsp_key_Config();
# endif
	
# if USE_LED > 0u
	bsp_led_Config();		        
# endif
	
//# if USE_USART > 0u
//	bsp_UsartConfig();
//# endif
	
# if USE_MALLOC > 0u
		bsp_mem_Config();
# endif


}


/*
*********************************************************************************************************
*                                       bsp_SoftReset   
*
* Description: 软件复位内核
*             
* Arguments  : 无
*
* Reutrn     ：无
*
* Note(s)    : 无
*********************************************************************************************************
*/
void bsp_SoftReset(void)
{
	__set_FAULTMASK(1);
	NVIC_SystemReset();
}





